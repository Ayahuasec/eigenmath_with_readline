double
get_char_depth(int font_num, int char_num)
{
	int t;
	double d;
	CTFontRef f;

	if (font_num == ITALIC_FONT || font_num == SMALL_ITALIC_FONT)
		t = italic_tab[char_num];
	else
		t = roman_tab[char_num];

	f = get_font_ref(font_num);

	if (t)
		d = CTFontGetDescent(f);
	else
		d = CTFontGetLeading(f);

	return d;
}
